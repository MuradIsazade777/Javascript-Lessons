// Operators
const num1 = 10;
const num2 = 5;
console.log(num1 + num2); // 15
console.log(num1 - num2); // 5
console.log(num1 * num2); // 50
console.log(num1 / num2); // 2
console.log(num1 % num2); // 0
console.log(num1 > num2); // true
console.log(num1 == "10"); // true
console.log(num1 === "10"); // false